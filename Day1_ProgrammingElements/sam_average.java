public class sam_average {
    public static void main(String[] args) {
        int mathsmarks=94;
        int physicsmarks=95;
        int chemistrymarks=96;
        float averagemarks=(mathsmarks+physicsmarks+chemistrymarks)/3;
        float averagepercent=(averagemarks*100)/100;
        System.out.println("Sam average marks in PCM :"+averagepercent);
    }
}
